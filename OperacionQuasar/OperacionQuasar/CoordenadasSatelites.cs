using System;
using System.Collections.Generic;
using System.Text;

namespace OperacionQuasar
{
    class CoordenadasSatelites
    {
        public double xKenobi;
        public double yKenobi;
        public double xSkywalker;
        public double ySkywalker;
        public double xSato;
        public double ySato;

        public CoordenadasSatelites(double xKe, double yKe, double xSky, double ySky, double xSa, double ySa)
        {
            this.xKenobi = xKe;
            this.yKenobi = yKe;
            this.xSkywalker = xSky;
            this.ySkywalker = ySky;
            this.xSato = xSa;
            this.ySato = ySa;
        }

    }
}
